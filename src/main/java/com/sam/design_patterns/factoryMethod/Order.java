package com.sam.design_patterns.factoryMethod;

public abstract class Order {
	
	protected String name;
	protected int price;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}


}
